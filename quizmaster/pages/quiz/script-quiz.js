function audioStop() {
    soundPlay.classList.remove('sound-play-paused')
    answerPlay.classList.remove('answer-play-paused')
    audioCurrent.pause()
}

function audioPlay() {
    soundPlay.classList.add('sound-play-paused')
    answerPlay.classList.add('answer-play-paused')
    audioCurrent.play()
}

